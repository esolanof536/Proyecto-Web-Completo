import { BASE_PATH, apiVersion } from "./config";

export function agregarEmpleadoApi(data) {

    const url = `${BASE_PATH}/${apiVersion}/agregar-Empleados`;
    const params = {

        method: "POST",
        body: JSON.stringify(data),
        headers: {
            "Content-Type": "application/json"
        }

    };

    console.log(data);

    return fetch(url, params).then(response => {
        return response.json()
    }).then(result => {
        return result;
    }).catch(err => {
        return err;
    })
}


export function getEmpleadosApi() {
    const url = `${BASE_PATH}/${apiVersion}/empleados`;

    const params = {

        method: "GET",
        headers: {
            "Content-Type": "application/json"
        },
        mode: "cors"
    };

    return fetch(url, params).then(response => {
        return response.json()
    }).then(result => {
        return result;
    }).catch(err => {
        return err;
    })
}

