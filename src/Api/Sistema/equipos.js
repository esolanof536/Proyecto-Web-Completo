import { BASE_PATH, apiVersion } from "../config";

export function agregarEquiposApi(data) {

    const url = `${BASE_PATH}/${apiVersion}/agregar-EquipoUtencilios`;
    const params = {

        method: "POST",
        body: JSON.stringify(data),
        headers: {
            "Content-Type": "application/json"
        }

    };

    console.log(data);

    return fetch(url, params).then(response => {
        return response.json()
    }).then(result => {
        return result;
    }).catch(err => {
        return err;
    })
}


